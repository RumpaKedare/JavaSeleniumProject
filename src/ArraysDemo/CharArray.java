package ArraysDemo;

public class CharArray {

    public static void main(String[] args) {

        char[] c = new char[10];

        c[0] = 'H';
        c[1] = 'e';
        c[2] = 'l';
        c[3] = 'l';
        c[4] = 'o';
        c[5] = 'r';
        c[6] = 'W';
        c[7] = 'o';
        c[8] = 'r';
        c[9] = 'l';

        for (int i = 0; i < c.length; i++) {
            System.out.println(c[i]);
        }

    }
}
